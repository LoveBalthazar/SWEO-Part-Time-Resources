class TreeNode {
  constructor(value, left, right) {
    this.value = value;
    this.left = left;
    this.right = right;
  }
}

// Given a tree, find the node with the highest value at each
// level of the tree and return it in an array, with the root at the
// 0th index, and the highest value in the deepest level of the tree
// in the last index.

//        5
//       / \
//      4   7
//     / \   \
//    1   3   2
//       /    / \
//      8    4   9
//              / \
//             2   4

// Expected Output -> [ 5, 7, 3, 9, 4 ]

const findMaxEachLevel = (root) => {
  const finalArr = [];
  let queue = [root];

  while (queue.length) {
    const next = [];
		// [4, 7]
    let max;
    queue.forEach((node) => {
      if (!max || max < node.value) max = node.value;
      if (node.left) next.push(node.left);
      if (node.right) next.push(node.right);
    });

    queue = next;
    finalArr.push(max);
  }

  return finalArr;
};

